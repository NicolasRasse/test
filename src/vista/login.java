    private void btnIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIngresarActionPerformed
        String usuarios = txtUsuario.getText();
        String contraseña = txtContraseña.getText();
        if (usuarios.isEmpty() || contraseña.isEmpty()) {
            JOptionPane.showMessageDialog(null, "El Campo Usuario / Contraseña esta vacio");
        } else {
            if (usuarios.equals("Admin") && contraseña.equals("291282")) {
                mInicio mi = new mInicio();
                mi.setVisible(true);
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(null, "Su Usuario / Contraseña son incorrectas");
            }
        }
    }//GEN-LAST:event_btnIngresarActionPerformed

